class User {
  final String id;
  final String name;
  final int age;
  final double height;
  final double weight;
  final double bmi;
  final String fitnessGoal;

  User({
    required this.id,
    required this.name,
    required this.age,
    required this.height,
    required this.weight,
    required this.bmi,
    required this.fitnessGoal,
  });
}